func TestGenerateComposeExcludesServices(t *testing.T) {
	m := &manifest.Manifest{
		Services: []manifest.Service{
			{Name: "web", Image: "ghcr.io/example/web:latest", Sets: []string{"application"}},
			{Name: "db", Image: "postgres:17", Sets: []string{"database"}},
		},
		Sets: map[string]manifest.Set{
			"application": {Vars: []manifest.Var{{Key: "APP_ENV", Default: "production"}}},
			"database":    {Vars: []manifest.Var{{Key: "DB_NAME", Default: "app"}}},
		},
	}

	compose := GenerateCompose(m, ComposeOptions{ExcludeServices: []string{"db"}})

	if !strings.Contains(compose, "web:") {
		t.Fatalf("compose output missing included service\n%s", compose)
	}
	if strings.Contains(compose, "db:") {
		t.Fatalf("compose output still contains excluded service\n%s", compose)
	}
}

func TestGenerateComposeExcludesSelectedService(t *testing.T) {
	m := &manifest.Manifest{
		Services: []manifest.Service{
			{Name: "web", Image: "ghcr.io/example/web:latest", Sets: []string{"application"}},
			{Name: "db", Image: "postgres:17", Sets: []string{"database"}},
		},
		Sets: map[string]manifest.Set{
			"application": {Vars: []manifest.Var{{Key: "APP_ENV", Default: "production"}}},
			"database":    {Vars: []manifest.Var{{Key: "DB_NAME", Default: "app"}}},
		},
	}

	compose := GenerateCompose(m, ComposeOptions{ServiceName: "web", ExcludeServices: []string{"web"}})

	if strings.Contains(compose, "web:") {
		t.Fatalf("compose output still contains excluded selected service\n%s", compose)
	}
	if strings.Contains(compose, "db:") {
		t.Fatalf("compose output should not fall back to unrelated services\n%s", compose)
	}
	if !strings.Contains(compose, "services: {}") {
		t.Fatalf("compose output should contain an empty services mapping when all selected services are excluded\n%s", compose)
	}
}

func TestGenerateComposeOmitsSecretVars(t *testing.T) {
	m := &manifest.Manifest{
		Services: []manifest.Service{{
			Name:   "web",
			Image:  "ghcr.io/example/web:latest",
			Sets: []string{"application"},
		}},
		Sets: map[string]manifest.Set{
			"application": {
				Description: "App settings",
				Vars: []manifest.Var{
					{Key: "PUBLIC_URL", Default: "https://example.org"},
					{Key: "SECRET_KEY", Default: "changeme", Secret: "true"},
				},
			},
		},
	}

	compose := GenerateCompose(m, ComposeOptions{})

	if !strings.Contains(compose, "PUBLIC_URL") {
		t.Fatalf("compose output missing non-secret var\n%s", compose)
	}
	if strings.Contains(compose, "SECRET_KEY") {
		t.Fatalf("compose output should omit secret vars\n%s", compose)
	}
	if !strings.Contains(compose, "# ── application") {
		t.Fatalf("compose output missing set header for visible vars\n%s", compose)
	}
	if strings.Contains(compose, "changeme") {
		t.Fatalf("compose output should not contain secret default values\n%s", compose)
	}
}

func TestGenerateComposeEditableAndRequiredOutput(t *testing.T) {
	m := &manifest.Manifest{
		Services: []manifest.Service{{
			Name:   "web",
			Image:  "ghcr.io/example/web:latest",
			Sets: []string{"application"},
		}},
		Sets: map[string]manifest.Set{
			"application": {
				Vars: []manifest.Var{
					{Key: "EDITABLE_OPT", Default: "opt", Editable: "true", Required: "false"},
					{Key: "EDITABLE_REQ", Default: "req", Editable: "true", Required: "true"},
					{Key: "LOCKED_REQ", Default: "locked", Editable: "false", Required: "true"},
				},
			},
		},
	}

	defaultCompose := GenerateCompose(m, ComposeOptions{Flavor: "default"})
	if !strings.Contains(defaultCompose, "EDITABLE_OPT: \"${EDITABLE_OPT:-opt}\"") {
		t.Fatalf("default flavor missing editable optional fallback\n%s", defaultCompose)
	}
	if !strings.Contains(defaultCompose, "EDITABLE_REQ: \"${EDITABLE_REQ:-req}\"") {
		t.Fatalf("default flavor missing editable required fallback\n%s", defaultCompose)
	}
	if !strings.Contains(defaultCompose, "LOCKED_REQ: \"locked\"") {
		t.Fatalf("default flavor should hardcode non-editable var\n%s", defaultCompose)
	}

	coolifyCompose := GenerateCompose(m, ComposeOptions{Flavor: "coolify"})
	if !strings.Contains(coolifyCompose, "EDITABLE_OPT: \"${EDITABLE_OPT:-opt}\"") {
		t.Fatalf("coolify flavor missing editable optional fallback\n%s", coolifyCompose)
	}
	if !strings.Contains(coolifyCompose, "EDITABLE_REQ: \"${EDITABLE_REQ:?req}\"") {
		t.Fatalf("coolify flavor should use :? for editable required vars\n%s", coolifyCompose)
	}
	if !strings.Contains(coolifyCompose, "LOCKED_REQ: \"locked\"") {
		t.Fatalf("coolify flavor should hardcode non-editable var\n%s", coolifyCompose)
	}
}
